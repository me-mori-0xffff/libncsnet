#include "ncsnet/mac.h"

static i8 asciihex(u8 c)
{
  if (c >= '0' && c <= '9')
    return c - '0';
  if (c >= 'a' && c <= 'f')
    return c - 'a' + 10;
  if (c >= 'A' && c <= 'F')
    return c - 'A' + 10;
  return -1;
}

int mac_aton(mac_t *addr, const char *txt)
{
  int i = 0, a = 0, b = 0;
  for (; i < MAC_ADDR_LEN; i++) {
    a = asciihex(*txt++);
    if (a < 0)
      return -1;
    b = asciihex(*txt++);
    if (b < 0)
      return -1;
    addr->octet[i] = (a << 4) | b;
    if (i < 5) {
      if (*txt != ':')
	return -1;
      else
	txt++;
    }
  }
  return 0;
}
